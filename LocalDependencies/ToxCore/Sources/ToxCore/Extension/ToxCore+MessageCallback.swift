// –¢–∏–ø –¥–∞–Ω–Ω—ã—Ö –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ —Å–æ–æ–±—â–µ–Ω–∏–π
/// –ö–ª–∞—Å—Å –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞ —Å–æ–æ–±—â–µ–Ω–∏–π –∏ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –æ–±—Ä–∞—Ç–Ω—ã—Ö –≤—ã–∑–æ–≤–æ–≤.
/// - `callback`: –ó–∞–º—ã–∫–∞–Ω–∏–µ, –∫–æ—Ç–æ—Ä–æ–µ –±—É–¥–µ—Ç –≤—ã–∑–≤–∞–Ω–æ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è.
/// - `init(callback:)`: –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç –æ–±—ä–µ–∫—Ç —Å –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–º –∑–∞–º—ã–∫–∞–Ω–∏–µ–º.
final class MessageContext {
  var callback: (Int32, String) -> Void
  
  /// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç –æ–±—ä–µ–∫—Ç `MessageContext` —Å –∑–∞–¥–∞–Ω–Ω—ã–º –∑–∞–º—ã–∫–∞–Ω–∏–µ–º.
  /// - Parameter callback: –ó–∞–º—ã–∫–∞–Ω–∏–µ, –∫–æ—Ç–æ—Ä–æ–µ –±—É–¥–µ—Ç –≤—ã–∑–≤–∞–Ω–æ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è.
  init(callback: @escaping (Int32, String) -> Void) {
    self.callback = callback
  }
}

// –ì–ª–æ–±–∞–ª—å–Ω–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–Ω—Ç–µ–∫—Å—Ç–∞
var globalConnectionMessageContext: MessageContext?

// –ì–ª–æ–±–∞–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –æ–±—Ä–∞—Ç–Ω–æ–≥–æ –≤—ã–∑–æ–≤–∞ –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π
/// –ì–ª–æ–±–∞–ª—å–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏–π –æ—Ç –¥—Ä—É–∑–µ–π –≤ —Å–µ—Ç–∏ Tox.
/// –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è —Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ—Ç—Å—è –≤ Tox –∫–∞–∫ –∫–æ–ª–ª–±–µ–∫ –∏ –≤—ã–∑—ã–≤–∞–µ—Ç—Å—è –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Å–æ–æ–±—â–µ–Ω–∏—è.
/// - Parameters:
///   - tox: –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Ç–µ–∫—É—â–∏–π –æ–±—ä–µ–∫—Ç Tox.
///   - friendNumber: –£–Ω–∏–∫–∞–ª—å–Ω—ã–π –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä –¥—Ä—É–≥–∞, –æ—Ç –∫–æ—Ç–æ—Ä–æ–≥–æ –±—ã–ª–æ –ø–æ–ª—É—á–µ–Ω–æ —Å–æ–æ–±—â–µ–Ω–∏–µ.
///   - messageType: –¢–∏–ø —Å–æ–æ–±—â–µ–Ω–∏—è, –æ–ø—Ä–µ–¥–µ–ª—è—é—â–∏–π, –∫–∞–∫ –æ–Ω–æ –¥–æ–ª–∂–Ω–æ –±—ã—Ç—å –∏–Ω—Ç–µ—Ä–ø—Ä–µ—Ç–∏—Ä–æ–≤–∞–Ω–æ (–Ω–∞–ø—Ä–∏–º–µ—Ä, —Ç–µ–∫—Å—Ç–æ–≤–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ, –¥–µ–π—Å—Ç–≤–∏–µ –∏ —Ç.–¥.).
///   - messagePtr: –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –±–∞–π—Ç—ã —Å–æ–æ–±—â–µ–Ω–∏—è. –ï—Å–ª–∏ –∑–Ω–∞—á–µ–Ω–∏–µ nil, —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–µ –±—ã–ª–æ –ø–æ–ª—É—á–µ–Ω–æ.
///   - length: –î–ª–∏–Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ –±–∞–π—Ç–∞—Ö.
///   - userData: –£–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–µ –¥–∞–Ω–Ω—ã–µ, –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã–µ –≤–º–µ—Å—Ç–µ —Å –∫–æ–ª–ª–±–µ–∫–æ–º.
let messageCallback: @convention(c) (
  UnsafeMutablePointer<Tox>?,
  Tox_Friend_Number,
  TOX_MESSAGE_TYPE,
  UnsafePointer<UInt8>?,
  size_t,
  UnsafeMutableRawPointer?
) -> Void = { tox, friendNumber, messageType, messagePtr, length, userData in
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ –Ω–µ —Ä–∞–≤–µ–Ω nil –∏ –¥–ª–∏–Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –±–æ–ª—å—à–µ –Ω—É–ª—è
  guard let messagePtr = messagePtr, length > 0 else { return }
  
  // –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —É–∫–∞–∑–∞—Ç–µ–ª—å –Ω–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏–µ –¥–∞–Ω–Ω—ã–µ –Ω–µ —Ä–∞–≤–µ–Ω nil
  guard let userData = userData else { return }
  
  // –ü–æ–ª—É—á–∞–µ–º –∫–æ–Ω—Ç–µ–∫—Å—Ç –∏–∑ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏—Ö –¥–∞–Ω–Ω—ã—Ö
  guard let context = globalConnectionMessageContext else {
    print("üî¥ –û—à–∏–±–∫–∞: –∫–æ–Ω—Ç–µ–∫—Å—Ç –Ω–µ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω")
    return
  }
  
  // –°–æ–∑–¥–∞–µ–º Data –∏–∑ —É–∫–∞–∑–∞—Ç–µ–ª—è –Ω–∞ –±–∞–π—Ç—ã —Å–æ–æ–±—â–µ–Ω–∏—è
  let messageData = Data(bytes: messagePtr, count: Int(length))
  
  // –ü—Ä–µ–æ–±—Ä–∞–∑—É–µ–º Data –≤ —Å—Ç—Ä–æ–∫—É –∏ –≤—ã–∑—ã–≤–∞–µ–º –∑–∞–º—ã–∫–∞–Ω–∏–µ —Å –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–º –¥—Ä—É–≥–∞ –∏ —Ç–µ–∫—Å—Ç–æ–º —Å–æ–æ–±—â–µ–Ω–∏—è
  if let messageStr = String(data: messageData, encoding: .utf8) {
    context.callback(Int32(friendNumber), messageStr)
  }
}
